// キャラ表示HTML生成
// key には MBTI(INTJ等) または レアキー(rare_cursed等) を渡す
function getCharHTML(key) {
  if (CHAR_IMG[key]) {
    return `<img src="${CHAR_IMG[key]}" alt="${key}" style="width:100%; height:100%; object-fit:contain;">`;
  }
  // フォールバック: レアキーが渡されたが画像がない場合は対応するMBTIキャラを使う
  if (typeof RARE_TYPES !== 'undefined' && RARE_TYPES[key]) {
    const baseMbti = RARE_TYPES[key].char;
    if (CHAR_IMG[baseMbti]) {
      return `<img src="${CHAR_IMG[baseMbti]}" alt="${baseMbti}" style="width:100%; height:100%; object-fit:contain;">`;
    }
  }
  return '';
}

